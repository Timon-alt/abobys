        itemCount: 10,
        itemBuilder: (context, i) => ListTile(
          
          title: Text(
            "Bitcoin", 
            style: Theme.of(context).textTheme.bodyMedium,),
          subtitle: Text(
            "200000\$",
            style: Theme.of(context).textTheme.labelSmall,
          ),
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
